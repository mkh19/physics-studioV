/**
 * Defines the anchor point used for positioning.
 */
export enum Anchor {

    /**
     * Top-left corner.
     */
    TopLeft,

    /**
     * Top center.
     */
    Top,

    /**
     * Top-right corner.
     */
    TopRight,

    /**
     * Middle left.
     */
    Left,

    /**
     * Object center.
     */
    Center,

    /**
     * Middle right.
     */
    Right,

    /**
     * Bottom-left corner.
     */
    BottomLeft,

    /**
     * Bottom center.
     */
    Bottom,

    /**
     * Bottom-right corner.
     */
    BottomRight

}