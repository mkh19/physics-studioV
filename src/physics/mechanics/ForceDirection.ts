/**
 * Represents the direction of a force.
 */
export enum ForceDirection {

    /**
     * Positive X direction.
     */
    Right,

    /**
     * Negative X direction.
     */
    Left,

    /**
     * Negative Y direction.
     */
    Up,

    /**
     * Positive Y direction.
     */
    Down,

    /**
     * Arbitrary direction.
     */
    Custom

}